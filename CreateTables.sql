create table Artist(
      artist_type varchar2(100) default null,
      artist_name varchar2(100)  default null, 
      artist_gender varchar2(100)  default null,
      artist_country varchar2(100)  default null
      );
     
create table Releases( 
      Release_title varchar2(100)  default null,
      Release_status varchar2(100) default null,
      Release_date varchar2(100)  default null,
      Release_format varchar2(100)  default null,
      Release_country varchar2(100)  default null
         
);